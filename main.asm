INCLUDE "hardware.inc"


SECTION "Header", ROM0[$100]

EntryPoint:
	di
	jp Start

REPT $150 - $104
	db 0
ENDR


SECTION "Game code", ROM0

Start:

.waitVBlank
	ld a, [rLY]
	cp 144
	jr c, .waitVBlank

	xor a
	ld [rLCDC], a
	ld hl, $9000
	ld de, bg_tiles
	ld bc, 48

.copyTiles
	ld a, [de]
	ld [hli], a
	inc de
	dec bc
	ld a, b
	or c
	jr nz, .copyTiles

	ld hl, $9800
	ld de, bg_map
	ld bc, 32*32
	
.copyMap
	ld a, [de]
	ld [hli], a
	inc de
	dec bc
	ld a, b
	or c
	jr nz, .copyMap

	ld a, %11100100
	ld [rBGP], a
	
	xor a
	ld [rSCX], a
	ld [rSCY], a

	ld [rNR52], a

	ld a, %10000001
	ld [rLCDC], a

.lockup
	jr .lockup


